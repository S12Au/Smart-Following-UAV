void W_SCL(uint8_t x){
	HAL_GPIO_WritePin(GPIOB,GPIO_PIN_8,x);
	Delay_us(10);
}

void W_SDA(uint8_t x){
	HAL_GPIO_WritePin(GPIOB,GPIO_PIN_9,x);
	Delay_us(10);
}

uint8_t R_SDA(){
	Delay_us(10);
	return HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_9);
}

void IIC_Start(){
	W_SDA(1);
	W_SCL(1);
	W_SDA(0);
	W_SCL(0);
}

void IIC_Stop(){
	W_SDA(0);
	W_SCL(1);
	W_SDA(1);
}

uint8_t IIC_ReceiveACK(){
	uint8_t ACK;
	W_SDA(1);
	W_SCL(0);
	W_SCL(1);
	ACK = R_SDA();
	W_SCL(0);
	return ACK;
}

void IIC_SendByte(uint8_t byte){
	for (uint8_t i =0; i<8; i++){
		W_SDA( byte & (0x80 >> i) );
		W_SCL(1);
		W_SCL(0);
	}
}

void IIC_SendACK(uint8_t ACK){
	W_SDA(ACK);
	W_SCL(1);
	W_SCL(0);
}

uint8_t IIC_ReceiveByte(){
	uint8_t byte=0;
	for (uint8_t i=0; i<8; i++){
		W_SCL(1);
		byte |= R_SDA();
		byte <<= 1;
		W_SCL(0);
	}
	byte >>= 1;
	return byte;
}
